package ForLoopDemo;

public class NestedLoopPractice {
    public static void main(String[] args) {

        //Example for nested loop
        for (int i = 0; i < 5; i++){
            System.out.println("i Count is: " + i);
            for (int j = 0; j < 5; j++){
                System.out.println("J Count is: " + j);
            }
        }
    }
}
